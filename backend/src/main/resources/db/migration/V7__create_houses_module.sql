-- ============================================================
-- My House module: houses, house_services, bills, bill_payments
-- ============================================================

CREATE TABLE houses (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    house_name VARCHAR(100) NOT NULL,
    address VARCHAR(255),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_houses_user FOREIGN KEY (user_id) REFERENCES users(id)
);

CREATE TABLE house_services (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    house_id BIGINT NOT NULL,
    service_name VARCHAR(100) NOT NULL,
    provider_name VARCHAR(100),
    account_number VARCHAR(100),
    billing_cycle VARCHAR(20) NOT NULL DEFAULT 'MONTHLY',
    average_amount DECIMAL(19, 4),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_house_services_house FOREIGN KEY (house_id) REFERENCES houses(id) ON DELETE CASCADE
);

CREATE TABLE bills (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    service_id BIGINT NOT NULL,
    amount DECIMAL(19, 4) NOT NULL,
    due_date DATE NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'PENDING',
    description VARCHAR(255),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_bills_service FOREIGN KEY (service_id) REFERENCES house_services(id) ON DELETE CASCADE
);

CREATE TABLE bill_payments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    bill_id BIGINT NOT NULL,
    account_id BIGINT NOT NULL,
    transaction_id BIGINT,
    amount DECIMAL(19, 4) NOT NULL,
    payment_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_bill_payments_bill FOREIGN KEY (bill_id) REFERENCES bills(id) ON DELETE CASCADE,
    CONSTRAINT fk_bill_payments_account FOREIGN KEY (account_id) REFERENCES accounts(id),
    CONSTRAINT fk_bill_payments_transaction FOREIGN KEY (transaction_id) REFERENCES transactions(id)
);

